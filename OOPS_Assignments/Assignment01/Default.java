
// WAP to declare static variables of all primitive data types and 
// print their default values


class Default
{	
   static boolean a;
		static byte b;
		static short c;
		static int d;
		static char e;
		static float f;
		static double g;
		static String h;
		 
		
	public static void main(String[]args)
	{	
		System.out.println("Default Value of boolean is "+a);
		System.out.println("Default Value of byte is "+b);
		System.out.println("Default Value of short is "+c);
		System.out.println("Default Value of int is "+d);
		System.out.println("Default Value of char is "+e);
		System.out.println("Default Value of float is "+f);
		System.out.println("Default Value of double is "+g);
		System.out.println("Default Value of String is "+h);
		
	}
}